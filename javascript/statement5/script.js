// 5-> Determine the grade based on a score

let score=prompt("enter total score")
console.log("score is:",+score);
if(score >=90){
    console.log("A1")
}
else if(score >=80){
    console.log("A2")
}
else if(score >=70){
    console.log("B1")
}
else if(score >=60){
    console.log("B2")
}
else if(score >=50){
    console.log("C1")
}
else if(score >=40){
    console.log("C2")
}
else{
    console.log("Fail");
    
}


